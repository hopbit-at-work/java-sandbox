package hfj.r03;
class TypyPodstawowe{
	public static void main(String[] args){
		int x;
		x=234;
		System.out.println("int x: "+x);
		byte b=89;
		System.out.println("byte b: "+b);
		boolean jestCzadowo=true;
		System.out.println("boolean jestCzadowo: "+jestCzadowo);
		double d=3243.98;
		System.out.println("double d: "+d);
		char c='f';
		System.out.println("char c: "+c);
		int z=x;
		System.out.println("int z: "+z);
		boolean hipHopRulez=false;
		System.out.println("boolean hipHopRulez: "+hipHopRulez);
		boolean zasilanie;
		zasilanie=jestCzadowo;
		System.out.println("boolean zasilanie: "+zasilanie);
		long duzy=9257892;
		System.out.println("long duzy: "+duzy);
		float f=32.4f;
		System.out.println("float f: "+f);
	}
}
